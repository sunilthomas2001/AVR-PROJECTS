/*
 * Clear Display
 */

void OLED_Clear(void)
{
	uint8_t page;
	uint8_t column;

	for(page = 0; page < 8; page++)
	{
		OLED_SetCursor(page, 0);

		for(column = 0; column < 128; column++)
		{
			OLED_Data(0x00);
		}
	}
}


/*
 * Set Cursor Position
 */
void OLED_SetCursor(uint8_t page,
                    uint8_t column)
{
    OLED_Command(0xB0 + page);

    OLED_Command(0x00 + (column & 0x0F));

    OLED_Command(0x10 + ((column >> 4) & 0x0F));
}

/*
 * Print One Character
 */
void OLED_PrintChar(char c)
{
    uint8_t i;

    if(c == ' ')
    {
        for(i=0;i<5;i++)
        {
            OLED_Data(0x00);
        }

        OLED_Data(0x00);

        return;
    }

    if(c >= '0' && c <= '9')
    {
        c = c - '0' + 1;
    }
    else if(c >= 'A' && c <= 'Z')
    {
        c = c - 'A' + 11;
    }
    else if(c == ':')
    {
        c = 37;
    }
    else if(c == '.')
    {
        c = 38;
    }
    else if(c == '-')
    {
        c = 39;
    }
    else
    {
        c = 0;
    }

    for(i=0;i<5;i++)
    {
        OLED_Data(font5x7[(uint8_t)c][i]);
    }

    OLED_Data(0x00);
}

/*
 * Print String
 */
void OLED_PrintString(char *str)
{
    while(*str)
    {
        OLED_PrintChar(*str);
        str++;
    }
}